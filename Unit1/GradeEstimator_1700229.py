
# Ask the user for their first name so we can store it and use it later in the program
first_name = input("Enter your first name: ")

# Ask the user for their last name so we can combine it with first name for full name output
last_name = input("Enter your last name: ")

# Print the full name so the user can see their first and last name together in proper format
print(first_name + " " + last_name)
